package Operators;

public class Arithmetic {

    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        int z;

        z = y - x;
        int z1 = y + x;
        int z2 = y * x;
        int z3 = y / x;
        int z4 = z3 * x;

        System.out.println(z);
        System.out.println(z1);
        System.out.println(z2);
        System.out.println(z3);
        System.out.println(z4);

    }
}
